    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (btnAdicionar == evt.getSource()){
            try {
                if((jtfNome.getText().isEmpty() || jtfSenha.getText().isEmpty())){
                    JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Atenção", INFORMATION_MESSAGE);
                } else {
                    FuncionarioDao funcionarioDao = new FuncionarioDao();
                    Funcionario funcionario = new Funcionario();
                    
                    funcionario.setNome(jtfNome.getText());
                    funcionario.setSenha(jtfSenha.getText());
                    funcionarioDao.salvar(funcionario);
                    limpar();
                    preencherTabela(funcionarioDao.listar(funcionario));
                    
                }
            } catch (Exception ex) {
                Logger.getLogger(TelaFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tabFuncionarios.getSelectedRow()!=-1){
            DAO dao = new DAO(); 
            FuncionarioDao funcionarioDao = new FuncionarioDao(); 
            Funcionario funcionario = new Funcionario();
                try {
                    funcionario.setId(Long.parseLong(jtfId.getText()));
                    funcionario.setNome(jtfNome.getText());
                    funcionario.setSenha(jtfSenha.getText());
                    funcionarioDao.salvar(funcionario);
                    JOptionPane.showConfirmDialog(null, "Editado com sucesso","", JOptionPane.DEFAULT_OPTION, 1); 
                    preencherTabela(funcionarioDao.listar(funcionario));
                    limpar();
                }        
                catch (Exception ex) {
                    Logger.getLogger(TelaFuncionarios.class.getName()).log(Level.SEVERE, null, ex);  
                }    
        } else {
            JOptionPane.showConfirmDialog(null, "Nenhum funcionario foi selecionado","", JOptionPane.DEFAULT_OPTION, 1);      
        }
        btnAdicionar.setEnabled(true);
        btnVoltar.setEnabled(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       int retorno = JOptionPane.showConfirmDialog(rootPane,"Deseja realmente eliminar?");
        if (retorno ==JOptionPane.OK_OPTION){
            try {
                 DAO dao = new DAO(); 
                 FuncionarioDao funcionarioDao = new FuncionarioDao(); 
                 Funcionario funcionario = new Funcionario();
                 funcionarioDao.eliminar(Funcionario.class, Long.parseLong(jtfId.getText()));
                 JOptionPane.showConfirmDialog(null, "Apagado com sucesso","", JOptionPane.DEFAULT_OPTION, 1);   
                 preencherTabela(funcionarioDao.listar());
                 limpar(); 
            } catch (Exception ex) {
                ex.printStackTrace();
            }    
              } else {
            JOptionPane.showConfirmDialog(null, "Nenhum funcionario selecionado","", JOptionPane.DEFAULT_OPTION, 1);   
        } 
         btnAdicionar.setEnabled(true);
        btnVoltar.setEnabled(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

