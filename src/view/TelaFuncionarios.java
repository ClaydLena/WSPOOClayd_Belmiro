    private void preencherTabela(ArrayList<Funcionario> funcionarios){
         DefaultTableModel model = new DefaultTableModel();
         tabFuncionarios.setModel(model);
         model.addColumn("ID");
         model.addColumn("Nome");
         model.addColumn("Senha");
         tabFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(10);
         
        for (Funcionario funcionario : funcionarios) {
            model.addRow(new Object[]{ funcionario.getId(), funcionario.getNome(), funcionario.getSenha()});
        }
     }
  
     public void limpar(){
            jtfNome.setText("");
            jtfSenha.setText("");
     }
