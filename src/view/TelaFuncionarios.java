    private void tabFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabFuncionariosMouseClicked
        btnVoltar.setEnabled(false);
        btnAdicionar.setEnabled(false);
        
        int i = tabFuncionarios.getSelectedRow();
        TableModel tabmodel = tabFuncionarios.getModel();
        jtfId.setText(Long.toString((Long) tabmodel.getValueAt(i,0)));
        jtfNome.setText((String) tabmodel.getValueAt(i, 1));
        jtfSenha.setText((String) tabmodel.getValueAt(i, 2));
    }//GEN-LAST:event_tabFuncionariosMouseClicked

